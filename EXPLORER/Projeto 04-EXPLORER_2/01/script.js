let number_1 = prompt('Digite o primeiro númer aqui:')
let number_2 = prompt('Digite o segundo número aqui:')

soma = Number(number_1) + Number(number_2)
subtracao = Number(number_1) - Number(number_2)
mult = Number(number_1) * Number(number_2)
div = Number(number_1) / Number(number_2)
rest = Number(number_1) % Number(number_2)

alert('A soma dos números 1 e 2 é igual a : ' + soma + '!!!')
alert('A subtração dos números 1 e 2 é igual a : '+ subtracao + '!!!')
alert('A multiplicação dos números 1 e 2 é igual a : '+ mult + '!!!')
alert('A divisão dos números 1 e 2 é igual a : '+ div + '!!!')
alert('O resto da divisão entre 1 e 2 é igual a: '+ rest)